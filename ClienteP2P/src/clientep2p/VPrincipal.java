    private void jButton_iniciarChatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_iniciarChatActionPerformed
        
            try {
                if(!this.jList_AmigosConectados.isSelectionEmpty()){
                    //Obtenemos el userID del amigo seleccionado para obtener el acceso a su interfaz
                    String userID_de_mi_amigo = this.jList_AmigosConectados.getSelectedValue();
                    ClientInterface amigo = this.friendsConnected.get(userID_de_mi_amigo);

                    //Iniciamos un chat entre los dos amigos
                    cliente.startChat(amigo, userID_de_mi_amigo);
                    amigo.startChat(cliente, userID);
                }
            } catch (RemoteException ex) {
                Logger.getLogger(VPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_jButton_iniciarChatActionPerformed

    private void jButton_enviarPeticionAmistadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_enviarPeticionAmistadActionPerformed
        
        try {
            if(!jList_ResultadoBusquedaUsuarios.isSelectionEmpty())
                this.servidor.friendRequest(userID, password, jList_ResultadoBusquedaUsuarios.getSelectedValue().toString());
        } catch (RemoteException ex) {
            Logger.getLogger(VPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton_enviarPeticionAmistadActionPerformed

    private void jButton_eliminarAmistadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_eliminarAmistadActionPerformed
        
        try {
            if(!this.jList_ListaAmigosDelUsuario.isSelectionEmpty()){
            
                boolean eliminado = false;
                String userID_ExAmigo = this.jList_ListaAmigosDelUsuario.getSelectedValue().toString();
                ArrayList<String> amigos = cliente.getListOfFriends();
                
                for(int i=0; i<amigos.size() && eliminado==false; i++){
                
                    if(amigos.get(i).toString().equals(userID_ExAmigo)){
                    
                        this.servidor.deleteFriend(userID_ExAmigo, userID, password);
                        eliminado=true;
                    }
                }
            }
            
        } catch (RemoteException ex) {
            Logger.getLogger(VPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton_eliminarAmistadActionPerformed

    private void jButton_buscarUsuariosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_buscarUsuariosActionPerformed
        
        List<String> listaBusquedaDeUsuarios = new ArrayList<>();
        try{
        
            listaBusquedaDeUsuarios = this.servidor.findFriend(this.textoBusquedaDeUsuarios.getText());
        }catch(RemoteException ex){
            System.out.println("Excepcion al buscar usuarios: "+ex.getMessage());
        }
        
        DefaultListModel listModel =new DefaultListModel();
        for(int i=0;i<listaBusquedaDeUsuarios.size();i++){
            listModel.add(i, listaBusquedaDeUsuarios.get(i));
        }
        this.jList_ResultadoBusquedaUsuarios.setModel(listModel);
        
        
    }//GEN-LAST:event_jButton_buscarUsuariosActionPerformed

    private void textoBusquedaDeUsuariosKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_textoBusquedaDeUsuariosKeyReleased
        
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            List<String> listaUsuarios=new ArrayList<>();
            try{
                listaUsuarios = servidor.findFriend(this.textoBusquedaDeUsuarios.getText());
            }catch(RemoteException ex){
                System.out.println("Excepcion al buscar usuarios: "+ex.getMessage());
            }

            DefaultListModel listModel = new DefaultListModel();
            for(int i=0; i<listaUsuarios.size(); i++){
                listModel.add(i, listaUsuarios.get(i));
            }
            this.jList_ResultadoBusquedaUsuarios.setModel(listModel);

        }
    }//GEN-LAST:event_textoBusquedaDeUsuariosKeyReleased


